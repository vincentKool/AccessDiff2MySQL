    private void jbtnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnStartActionPerformed
    // Check for old and new locations
        if(!(   (oldDBLocation != null) &&
                (newDBLocation != null) &&
                (!oldDBLocation.isEmpty()) &&
                (!newDBLocation.isEmpty()))) {
            jTextArea1.setText("First choose two database files");
        } else {
            main = new MSAccessDiffAndSQLScripter(oldDBLocation, newDBLocation, this);
            scripterThread = new Thread(main);
            jbtnStart.setEnabled(false);
            jCancelBtn.setEnabled(true);
            scripterThread.start();
            
        }
    // Initialize scripter
    }//GEN-LAST:event_jbtnStartActionPerformed

    private void jCancelBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCancelBtnActionPerformed
        if(scripterThread != null){
            scripterThread.interrupt();
            jbtnStart.setEnabled(true);
            jCancelBtn.setEnabled(false);
        }
            
    }//GEN-LAST:event_jCancelBtnActionPerformed

