    public void updateStatusBarAndTextArea(float value, String message){
        float val = jpbProgress.getMaximum()*value;
        jpbProgress.setValue((int) Math.rint(Math.ceil(val)));
        jTextArea1.append(message+"\n");
        //this.update(jpbProgress.getGraphics());
        if(value == 1.0)
        {
            jbtnStart.setEnabled(true);
            jCancelBtn.setEnabled(false);
        }
        //this.getGraphics()
    }
        
