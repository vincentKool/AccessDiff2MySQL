    private void jbtnBrowseOldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnBrowseOldActionPerformed
        int returnVal = jfcOLD.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = jfcOLD.getSelectedFile();
            oldDBLocation = file.getAbsolutePath();
            jtfOldLoc.setText(oldDBLocation);
        } else {
            Logger.getLogger("net.weaponsystems.SQLScripter").fine("File access cancelled by user.");
        }
    }//GEN-LAST:event_jbtnBrowseOldActionPerformed

